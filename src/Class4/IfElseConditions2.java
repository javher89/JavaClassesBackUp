package Class4;

public class IfElseConditions2 {
    public static void main(String[] args) {

        boolean rain=true;

        if(rain) {
            System.out.println("let's take an umbrella");
        }else{
            System.out.println("Let's leave the umbrella at home");
        }

        boolean breakTime=true;

        if(breakTime){
            System.out.println("Let's go on break");
        }else{
            System.out.println("Let's continue the class");
        }

    }
}
