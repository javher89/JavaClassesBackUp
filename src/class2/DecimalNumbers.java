package class2;

public class DecimalNumbers {
    public static void main(String[] args) {
        /*
        if we want to store decimal numbers (10.5 110.2 1.6) in the computer's memory using java we can use
        the following types
        float
        decimal
         */
        float myNumberBox=10.5f;
        float mySecondBox=5.5f;
        double largeDecimalBox=124213.121;
        System.out.println(myNumberBox);
        System.out.println(mySecondBox);
        System.out.println(largeDecimalBox);

        char letter='f';
        char gender='M';
        char symbol='%';
        char numberChar='1';
        System.out.println(letter);
        System.out.println(numberChar);
        /*
        if we want to store characters we can use char datatype
         */

        String name="Sam";
        System.out.println(name);
        /*
        String data type belongs to a different category called non-primitives which is why String rules
        are a little different, for example S of String is upper case it's color is also not blue.
         */
        boolean hungry=true; // true means yes
        boolean sleepy=false; //false means no

    }
}
