package class2;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number = 10;
        // int number = 20; we can't name two boxes with the same name, it confused java
        System.out.println(number);

        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        //select println and press CMD+D to duplicate
        System.out.println(number1*number2);
        System.out.println(number1/number2);
        /*
        if you store whole numbers always use int
        if you store decimal numbers always use double
        if you store characters you can always use String
         */
    }
}
