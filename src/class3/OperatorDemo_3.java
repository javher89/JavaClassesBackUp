package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3); //3
        System.out.println(number%3); //1

        System.out.println(16/5);
        System.out.println(16%5); // use modulus when you want to find out what is left after equal parts are disbursed

        System.out.println(156%2); //remainder is 0, modulus analysis concludes 0; it's an even number
        System.out.println(155%2);
        System.out.println(1555%5);// you can use modulus to see if a variable is completely dividable by another
    }
}
